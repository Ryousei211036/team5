#include"result.h"
#include"inputkeyboard.h"
#include"application.h"
#include"fade.h"
#include"game.h"

//==============================================================================================
// コンストラクタ
//==============================================================================================
CResult::CResult(int nPriority) : CObject(nPriority)
{
}

//==============================================================================================
// デストラクタ
//==============================================================================================
CResult::~CResult()
{
}

//==============================================================================================
// 初期化処理
//==============================================================================================
HRESULT CResult::Init()
{
	return S_OK;
}

//==============================================================================================
// 終了処理
//==============================================================================================
void CResult::Uninit()
{
	Release();
}

//==============================================================================================
//更新処理
//==============================================================================================
void CResult::Update()
{
	CInputkeyboard *pKeyboard = CApplication::GetInputKeyboard();

	if (pKeyboard->GetKeyboardTrigger(DIK_RETURN))
	{
		CFade::SetFade(CApplication::MODE_TITLE, 0.05f);
	}
}
//==============================================================================================
// 描画処理
//==============================================================================================
void CResult::Draw()
{

}

//==============================================================================================
// 生成処理
//==============================================================================================
CResult *CResult::Create()
{
	CResult *pResult = nullptr;

	pResult = new CResult;

	if (pResult != nullptr)
	{
		pResult->Init();
	}

	return pResult;
}